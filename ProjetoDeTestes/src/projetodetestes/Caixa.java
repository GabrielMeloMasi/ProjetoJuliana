    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        try {
             // Esse botão realiza o salvamento de tudo o que foi escrito nas textbox
             //Produto,descrição,preço e quantidadde
            Connection con = Conexao.faz_conexao();
            String sql = "insert into produto(NomeProduto, Descricao, valorUnitario, QntdProduto) values (?, ?, ? , ?)";

            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, Nome_Do_Produto.getText());
            stmt.setString(2, Desc_Produto.getText());
            stmt.setString(3, Preço_unit.getText());
            stmt.setString(4, Quantidade.getText());

            stmt.execute();
            stmt.close();
            con.close();
            JOptionPane.showMessageDialog(null,"Cadastrado!");
            Nome_Do_Produto.setText("");
            Desc_Produto.setText("");
        } catch (SQLException e) {

            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton7ActionPerformed


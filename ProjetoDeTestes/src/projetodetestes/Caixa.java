    private void btnListarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarDadosActionPerformed
    // Conecta o codigo com o banco,puxando produtos,valores e quantidade
        try {
            Connection con = Conexao.faz_conexao();
            String sql = "select * from produto";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) tbProdutos.getModel();
            modelo.setNumRows(0);
            while (rs.next()) {

                modelo.addRow(new Object[]{rs.getString("idProduto"),rs.getString("NomeProduto"), rs.getString("Descricao"), rs.getString
                    ("valorUnitario"), rs.getString("QntdProduto")});

        }
        rs.close();
        con.close();//Fecha o codigo

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnListarDadosActionPerformed


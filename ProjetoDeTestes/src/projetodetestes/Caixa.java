    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        //Tela de cadastro,funcionário ou moderador,puxando usuário e senha baseados no banco
        
        if(tfBusca.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Informe o ID");
        }else{
            try {
                Connection con = Conexao.faz_conexao();
                String sql = "select * from dados_senhas where id like ?";
                PreparedStatement stmt = con.prepareStatement(sql);

                stmt.setString(1,"%"+tfBusca.getText());
                ResultSet rs = stmt.executeQuery();
                while (rs.next()) {
                    tfId.setText(rs.getString("id"));
                    tfUsuarioCadastro.setText(rs.getString("usuario"));
                    tfSenhaCadastro.setText(rs.getString("senha"));
                }
                rs.close();
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }}
    }//GEN-LAST:event_jButton6ActionPerformed

